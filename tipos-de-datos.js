// Práctica de conceptos básicos de Java Script con los siguientes valores:
console.log(typeof 42); // number
console.log(typeof 'Veinticinco'); // string
console.log(typeof -666); // number
console.log(typeof true); // boolean
console.log(typeof 0); // number
console.log(typeof ''); // string
console.log(typeof null); // object (es un error histórico de JS)
console.log(typeof undefined); // undefined 
console.log(typeof FALSE); // undefined (porque FALSE en mayúsculas no existe, se toma como variable no definida)

// Otros ejemplos:
console.log(typeof 3.14); // number
console.log(typeof [1, 2, 3]); // object (los arreglos son objetos)
console.log(typeof { nombre: 'Carolina' }); // object
console.log(typeof Symbol('id')); // symbol

